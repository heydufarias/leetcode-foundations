void main() {
  // Module 1 — Single Pointer
  // Before using multiple pointers, you must fully control one.
  // These exercises train how to move, stop, and make decisions using a single index.

  // Block 1.1 — Pointer Movement
  // Just move through the list. No decisions, no extra logic.

  // Block 1.2 — Stopping the Pointer
  // Now the pointer doesn’t go until the end. You decide when it stops.

  // Block 1.3 — Decision While Traversing
  // The pointer moves forward, but at each step it makes a decision.

  // Block 1.4 — Pointer as Position Memory
  // Now the pointer doesn’t just move — it remembers where something happened.

  // Block 1.5 — Conditional Skipping
  // The pointer moves different distances (i++ or i += n) based on a condition, skipping parts of the list.

  // Block 1.6 — Relational Pointer (Neighbor Awareness)
  // Decisions are made by comparing the current element with its neighbors (i + 1 or i - 1).

  // Block 1.7 — Fixed Sliding Window
  // The pointer is used as a base to access a fixed group of neighboring elements at the same time.

  // Block 1.8 — In-place Modification
  // Modify the original list while traversing it, without creating auxiliary lists.
}
